## The following functions help in storing the value
## of the inverse of the matrix so that they don't need to be calculated again.

## This function stores the value of the inverse.

makeCacheMatrix <- function(x = matrix()) {
  
  inv <- NULL
  set <- function(y) {
    x <<- y
    inv <<- NULL
  }
  get <- function() x
  setinv <- function(inverse) inv <<- inverse
  getinv <- function() inv
  list(set = set, get = get, setinv = setinv, getinv = getinv)
}


## This function looks up in the previous function for the inverse
## It's a time efficient manner of doing things.

